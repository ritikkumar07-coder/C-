/*OBJECT ORIENTED PROGRAMMING*/

/*OOPs (Object-Oriented Programming) in C++ is a paradigm that helps in 
 organizing complex programs by using objects and classes. 
 It allows modularity, reusability, and scalability.*/

/*Key Concepts of OOPs in C++
1-Class & Object – Class is a blueprint, and objects are instances of a class.

2-Encapsulation – Wrapping data and methods together while restricting direct access.

3-Inheritance – One class can inherit attributes and behavior from another.

4-Polymorphism – Ability to take multiple forms (Function Overloading & Overriding).

5-Abstraction – Hiding implementation details and exposing only essential features.

6-Constructor & Destructor – Special functions to initialize and clean up an object.

7-Friend Function – Function that can access private members of a class.

8-Operator Overloading – Redefining operators for user-defined types.

9-Virtual Functions – Functions in a base class that can be overridden in derived classes.

10-Dynamic Binding (Late Binding) – Method call resolved at runtime using virtual functions.

11-Message Passing – Objects communicate by sending messages (calling methods).

12-Interfaces (Abstract Classes) – Classes with at least one pure virtual function.*/